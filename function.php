<?php

class Calculator{

	function addition($AddOne, $AddTwo){
		echo "Addition = " . ($AddOne + $AddTwo). "<br>";
	}

	function subtraction($SubOne, $SubTwo){
		echo "Subtraction = " . ($SubOne - $SubTwo). "<br>";
	}

	function multiplication($MulOne, $MulTwo){
		echo "Multiplication = " . ($MulOne * $MulTwo). "<br>";
	}

	function division($DivOne, $DivTwo){
		echo "Division = " . ($DivOne / $DivTwo). "<br>";
	}

}